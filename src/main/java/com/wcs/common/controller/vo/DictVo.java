package com.wcs.common.controller.vo;

import java.io.Serializable;

public class DictVo implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 9057143947959813257L;
	
	private long id;   		     //ID
	private String codeCat;      //code的英文标识符
	private String codeKey;		 //code的数字标识符
	private String codeVal;		 //code的Valie值
	private String remarks;		 //备注
	private String seqNo;			 //序列号,顺序号
	private String sysInd;		 //系统标识
	private String lang;		 //语言
	private String defunctInd;   //状态
	
	//构造方法
	public DictVo(){
		
	}
	public DictVo(long id,String codeCat,String codeKey,String codeVal,String remarks,String seqNo,String sysInd, String lang, String defunctInd){
		this.id=id;
		this.codeCat=codeCat;
		this.codeKey=codeKey;
		this.codeVal=codeVal;
		this.remarks=remarks;
		this.seqNo=seqNo;
		this.sysInd=sysInd;
		this.lang=lang;
		this.defunctInd=defunctInd;
	}
	
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getCodeCat() {
		return codeCat;
	}
	public void setCodeCat(String codeCat) {
		this.codeCat = codeCat;
	}
	public String getCodeKey() {
		return codeKey;
	}
	public void setCodeKey(String codeKey) {
		this.codeKey = codeKey;
	}
	public String getCodeVal() {
		return codeVal;
	}
	public void setCodeVal(String codeVal) {
		this.codeVal = codeVal;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getSeqNo() {
		return seqNo;
	}
	public void setSeqNo(String seqNo) {
		this.seqNo = seqNo;
	}
	public String getSysInd() {
		return sysInd;
	}
	public void setSysInd(String sysInd) {
		this.sysInd = sysInd;
	}
	public String getLang() {
		return lang;
	}
	public void setLang(String lang) {
		this.lang = lang;
	}
	public String getDefunctInd() {
		return defunctInd;
	}
	public void setDefunctInd(String defunctInd) {
		this.defunctInd = defunctInd;
	}
	
	
	
	
}
